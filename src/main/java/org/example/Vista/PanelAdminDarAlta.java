    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        //Extraemos todos los datos
        String usuario=Usuario.getText();
        String contrasenia=Contrasenia.getText();
        int indice=jComboBox1.getSelectedIndex();

        Verificaciones verificacion=new Verificaciones();
        Utilidades util=new Utilidades();

        //creamos los objectos
       Empleado empleado=new Empleado(usuario,contrasenia);
        Rol rol = null;


        if(!verificacion.verificacionRepetidosEmpleados(util.listaEmpleados(),empleado)) {


            switch (indice) {

                case 0:
                    rol = new Rol("Levantar quejas", new Empleado(usuario, contrasenia), "Maitres");
                    break;


                case 1:
                    rol = new Rol("Atiende el cliente", new Empleado(usuario, contrasenia), "Recepcionista");
                    break;

                case 2:
                    rol = new Rol("Administra el sistema", new Empleado(usuario, contrasenia), "Admnistrador");

                    break;


                default:

                    break;



            }
            Empleado empleados = new Empleado(contrasenia, rol, usuario);

            Admin admin = new Admin();

            admin.CrearCuenta(empleados);
        }else {


            JOptionPane.showMessageDialog(null, "Error usuario ya registrado", "Mensaje", JOptionPane.ERROR_MESSAGE);

        }








    }//GEN-LAST:event_jButton1ActionPerformed

